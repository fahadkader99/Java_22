package random;

public class dataType_Literal {
    public static void main(String[] args) {

        // int literal can be represented in any number system
        //like - binary, decimal, octal, hexadecimal

        // int literal
        byte b1 = 10;
        byte b2 = 0b1010;
        byte b3 = 012;
        byte b4 = 0XA;

        System.out.println(b1);
        System.out.println(b2);
        System.out.println(b3);
        System.out.println(b4);

        // Long literal
        long l = 125_4444484_8484884L;
        System.out.println(l);


        // double & float literal
        double d = 12.56D;
        float f = 12.56F;

        System.out.println(d);
        System.out.println(d);
    }

}

