package november_22;

public class SwapNum {
    // swap numbers with 3rd variable
    public static void main(String[] args) {
        int a = 2;
        int b = 4;
        int c = 0;

        c = a;
        a = b;
        b = c;

        System.out.println(a);
        System.out.println(b);
    }
}
