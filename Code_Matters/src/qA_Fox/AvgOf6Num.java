package qA_Fox;

import java.util.Scanner;

public class AvgOf6Num {
    // java program that takes 6 numbers as input and print their average

    public static void main(String[] args) {

        int a = 2;
        int b = 4;
        int c = 6;
        int d = 8;
        int e = 10;
        int f = 20;


        int sum = a+b+c+d+e+f;
        int av = sum / 6;
        System.out.println(av);



    }
}
