package qA_Fox;

public class OddNumTill100 {
    // print the odd num till 100;

    public static void main(String[] args) {

        System.out.println("\nPrinting Odd: ");
        for (int i = 0; i<=100;i++){
            if(i % 2 != 0){
                System.out.print(i+" ");
            }
        }

    }
}
